number = int(input('Insira um número inteiro e obtenha o dia da semana equivalente: '))

if number == 1:
    print('1 - Domingo')
elif number == 2:
    print('2 - Segunda-feira')
elif number == 3:
    print('3 - Terça-feira')
elif number == 4:
    print('4 - Quarta-feira')
elif number == 5:
    print('5 - Quinta-feira')
elif number == 6:
    print('6 - Sexta-feira')
elif number == 7:
    print('7 - Sabádo-feira')
else:
    print('Entrada invalida')
